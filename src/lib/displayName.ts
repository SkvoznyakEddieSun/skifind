/**
 * Короткое имя ученика для UI из полного ФИО.
 *
 * В БД хранится полное ФИО в порядке «Фамилия Имя Отчество»
 * (напр. «Мальгина Анастасия Петровна»). В интерфейсе показываем «Имя Фамилия»
 * (→ «Анастасия Мальгина»). Если имени нет — телефон, иначе «Ученик».
 *
 * ВНИМАНИЕ: применять только к именам учеников (полное ФИО). Имена инструкторов
 * хранятся уже в коротком виде «Имя Фамилия» — их не переставлять.
 */
export function shortStudentName(fullName: string | null | undefined, phone?: string | null): string {
  const parts = (fullName ?? '').trim().split(/\s+/).filter(Boolean);
  if (parts.length >= 2) return `${parts[1]} ${parts[0]}`;   // Имя Фамилия
  if (parts.length === 1) return parts[0];
  return phone || 'Ученик';
}
